#!/usr/bin/env node
/**
 * sync-mas-brain — regenerate public/mas-brain-data.json FROM the real
 * ~/.claude/skills/mas-coaching/SKILL.md file.
 *
 * Same single-source-of-truth bridge the CMO brain uses (see sync-skills.mjs):
 * the MAS Coaching Brain's identity/voice file lives on MAS's machine and is the
 * editable source of truth; this script reads it and writes a committed snapshot
 * into the repo so the app (which runs on Vercel and cannot read ~/.claude at
 * runtime) can use it for both the tab display and the query layer's system prompt.
 *
 * Run it whenever the skill changes:  node scripts/sync-mas-brain.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HOME = homedir();
const __dir = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dir, "..", "public", "mas-brain-data.json");
// Server-importable copy of the identity body, for the query layer's system prompt.
// (public/ is served by the CDN and not reliably readable from the server at runtime.)
const OUT_TS = join(__dir, "..", "src", "lib", "mas-brain", "identity.ts");
const SKILL_FILE = join(HOME, ".claude/skills/mas-coaching/SKILL.md");
const DISPLAY_PATH = "~/.claude/skills/mas-coaching/SKILL.md";

function parse(raw) {
  // Split YAML-ish frontmatter from the markdown body.
  let name = null, description = null, body = raw;
  const m = raw.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  if (m) {
    body = m[2];
    const fm = m[1];
    const nameM = fm.match(/^name:\s*(.+)$/m);
    if (nameM) name = nameM[1].trim();
    const dm = fm.match(/^description:\s*>?\s*\n?([\s\S]*?)(?:\n[a-zA-Z_]+:|$)/m);
    if (dm) description = dm[1].replace(/\n\s+/g, " ").trim();
  }
  return { name, description, body: body.trim() };
}

let raw;
try {
  raw = readFileSync(SKILL_FILE, "utf8");
} catch {
  console.warn(`[sync-mas-brain] missing: ${SKILL_FILE} — writing placeholder`);
  raw = "";
}
const { description, body } = parse(raw);

const out = {
  updated: new Date().toISOString().slice(0, 10),
  generatedBy: "scripts/sync-mas-brain.mjs — reads the real SKILL.md; do not hand-edit",
  brain: {
    id: "mas-coaching",
    name: "MAS Coaching Brain",
    tagline: "Handle it the way MAS would",
    path: DISPLAY_PATH,
    selfImproving: true,
    description: description || "",
    markdown: body || "_Skill file not found on disk._",
  },
};

writeFileSync(OUT, JSON.stringify(out, null, 2) + "\n");

const ts = `// GENERATED by scripts/sync-mas-brain.mjs from the real SKILL.md — do not hand-edit.
// Server-side copy of the MAS Coaching Brain identity, used as the query layer's system prompt.
export const MAS_BRAIN_UPDATED = ${JSON.stringify(out.updated)};
export const MAS_BRAIN_DESCRIPTION = ${JSON.stringify(out.brain.description)};
export const MAS_BRAIN_IDENTITY = ${JSON.stringify(out.brain.markdown)};
`;
writeFileSync(OUT_TS, ts);
console.log(`[sync-mas-brain] wrote ${OUT} and ${OUT_TS} (updated ${out.updated}, ${body.length} chars).`);
